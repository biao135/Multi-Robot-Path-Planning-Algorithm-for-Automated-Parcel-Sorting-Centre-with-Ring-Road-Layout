        }
        server.displayRobotCount(robotCount);


        double currentTime = 0;
        double stopTime = 36000;
        int lastThroughput = 0;
        StreamWriter sw;
        if(server.getCurveRoads().Count != 0){
            sw = new StreamWriter("123 With ring road - " + robotCount + " robots.txt");
        }
        else{
            sw = new StreamWriter("123 Without ring road - " + robotCount + " robots.txt");
        }

        Console.WriteLine("number of robots: " + robotCount);
        while (true){
            for(int i = 0; i < robotList.Count; i++)
            {
                robotList[i].Update();
            }
            currentTime += deltaTime;
            int serverThroughput = server.getThroughput();
            if (currentTime >= stopTime)
            {   
                Console.WriteLine("Simulation ended.");
                Console.WriteLine("Throughput: " + serverThroughput);
                sw.WriteLine("Throughput: " + serverThroughput);
                sw.Close();
                break;
            }
            else if (serverThroughput % 1000 == 0 && lastThroughput != serverThroughput)
            {
                Console.WriteLine("Time: " + currentTime + "\tParcels: " + serverThroughput + " Available charging points: " + server.getAvailableChargingPointCount());
                lastThroughput = serverThroughput;
            }
        }
    }
